struct Intro {
    static let INTRO_ENG = """

The Profane Earth is an attempt to redefine poetry in the 21st century.

How is one to think of, write, and conceptualize poetry when the medium is now so profoundly different?

How is one to redeploy and recreate poetic worlds and experiences while not just adding words to a multimedia platform, nor changing the poetic experience so much that nothing remains?

This is my attempt to explore these questions.

I think of poems as environments, even, and mostly, when I write them.

Using words, I try to create, within the reader’s mind, an aesthetic experience that creates an emotional landscape.

Words, to me, are tools to push perception and meaning into new arrangements and new conflicts out of which unusual poignant environments may rise.

Thanks to virtual reality, I can now truly explore these concepts.

I hope you will enjoy the experience.

These virtual worlds use poems taken from The Profane Earth, by Ollivier Dyens, Mansfield Press (2004)

"""
    
static let INTRO_FR = """

La Terre Profane est une tentative de redéfinir la poésie au 21ème siècle.

Comment penser, écrire et conceptualiser la poésie dans des environnements technologiques si radicalement différents de ce qu’offrent livre et papier? Comment redéployer et recréer des mondes et des expériences poétiques au-delà des mots et de la syntaxe, en utilisant les extraordinaire matériaux que sont le numérique, le virtuel, l’algorithme et l’immersif ?  Comment penser le rythme, la texture, l’émotion poétiques dans des mondes contrôlés par des algorithmes ?
 
Ces œuvres s’offrent à vous comme réponses partielles, et insuffisantes j’en conviens, à ces questions.

Pour moi, les poèmes sont des lieux, des territoires émotifs. En utilisant des mots, j'essaie de créer, dans l'esprit du lecteur, une expérience esthétique qui donne naissance à un paysage émotionnel.
 
Les mots, pour moi, sont des outils qui me permettent de faire pénétrer perception et signification dans des structures syntaxiques et émotionnelles différentes à partir desquelles des environnements poignants et inhabituels peuvent émerger.

Grâce à la réalité virtuelle, je peux maintenant littéralement explorer ces concepts.

J'espère que vous apprécierez l'expérience.

Ces mondes virtuels utilisent des poèmes tirés de La Terre profane, par Ollivier Dyens, publiés chez Mansfield Press (2004)

"""
}



